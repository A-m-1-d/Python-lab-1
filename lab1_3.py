# - Выводит все числа от n до 1 в обратном порядке.
# - Вычисляет факториал числа n.

n = int(input("Введите число n: "))

# Выводим числа от n до 1 в обратном порядке с помощью цикла while
print("Числа от", n, "до 1:")
i = n
while i >= 1:
    print(i, end=" ")
    i -= 1
print()

# Вычисляем факториал числа n (произведение чисел от 1 до n)
factorial = 1
i = 1
while i <= n:
    factorial *= i
    i += 1
print("Факториал числа", n, "равен:", factorial)

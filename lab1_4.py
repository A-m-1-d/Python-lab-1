# - Выводит исходный список.
# - Находит и выводит максимальное и минимальное значения.
# - Вычисляет и выводит сумму всех элементов списка.
# - Сортирует список по возрастанию и выводит его.

import random

# Создаем список из 10 случайных чисел от 1 до 100
numbers = [random.randint(1, 100) for _ in range(10)]

print("Исходный список:", numbers)

print("Максимальное число:", max(numbers))
print("Минимальное число:", min(numbers))

print("Сумма элементов списка:", sum(numbers))

# Сортируем список по возрастанию
sorted_numbers = sorted(numbers)
print("Отсортированный список:", sorted_numbers)

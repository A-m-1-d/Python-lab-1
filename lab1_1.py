# Лабораторная работа №1, Задание 1: Условные операторы
# Программа запрашивает три числа у пользователя и определяет:
# - Наибольшее из них
# - Наименьшее из них
# - Являются ли все числа равными

# Запрашиваем три числа у пользователя и преобразуем ввод в тип float
a = float(input("Введите первое число: "))
b = float(input("Введите второе число: "))
c = float(input("Введите третье число: "))

# Находим наибольшее и наименьшее число
max_num = max(a, b, c)
min_num = min(a, b, c)

# Проверяем, равны ли все три числа
all_equal = (a == b == c)

# Выводим результаты
print("Наибольшее число:", max_num)
print("Наименьшее число:", min_num)
if all_equal:
    print("Все числа равны.")
else:
    print("Не все числа равны.")
